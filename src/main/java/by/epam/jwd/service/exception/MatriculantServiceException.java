package by.epam.jwd.service.exception;

public class MatriculantServiceException extends ServiceException {

	private static final long serialVersionUID = 2302295194803849533L;

	public MatriculantServiceException() {
		super();
	}

	public MatriculantServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public MatriculantServiceException(String message) {
		super(message);
	}

	public MatriculantServiceException(Throwable cause) {
		super(cause);
	}
}
